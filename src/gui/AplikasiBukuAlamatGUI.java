    private void buttonImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonImportActionPerformed
        try {
            BufferedReader br = new BufferedReader(new FileReader("ImportData.txt")); // Membaca file "ImportData.txt" menggunakan BufferedReader
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); // Mengambil model tabel default
            Object[] lines = br.lines().toArray(); // Mengkonversi baris dari file menjadi array objek
            
            for (int i = 0; i < lines.length; i++) { // Looping melalui setiap baris, membaginya menjadi array string, dan menambahkannya ke model tabel
                String[] row = lines[i].toString().split(" ");
                model.addRow(row); // Menambahkan baris ke model tabel
                JOptionPane.showMessageDialog(null, "Data Berhasil Import");
            }
           
          br.close(); // Menutup BufferedReader setelah selesai membaca file
            
        } catch (Exception e) {
            System.err.println("Error: " + e.getMessage()); // Menangkap dan mencetak pesan kesalahan jika terjadi eksepsi
        }
    }//GEN-LAST:event_buttonImportActionPerformed

    private void buttonExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExportActionPerformed
        try {
            BufferedWriter bw = new BufferedWriter(new FileWriter("ExportData.txt")); // Membuat BufferedWriter untuk menulis ke file "ExportData.txt"
            
            for(int i = 0; i < jTable1.getRowCount(); i++) { // Loop baris
                for(int j = 0; j < jTable1.getColumnCount(); j++) { // Loop kolom
                    bw.write(jTable1.getValueAt(i, j).toString()+" "); // Menulis nilai dari setiap sel dalam tabel ke file, dipisahkan oleh spasi
                }
                
                bw.newLine(); // Menambahkan baris baru setelah menulis semua kolom untuk satu baris
            }
            
           JOptionPane.showMessageDialog(null, "Data Berhasil Export");
           bw.close(); // Menutup BufferedWriter setelah selesai menulis ke file
           
        } catch (Exception e) {
            System.err.println("Error: " + e.getMessage()); // Menangkap dan mencetak pesan kesalahan jika terjadi eksepsi
        }
    }//GEN-LAST:event_buttonExportActionPerformed

