    private void buttonTableHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTableHapusActionPerformed
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); // Mengambil model tabel default
       if (jTable1.getSelectedRow()==-1) { // Memeriksa apakah ada baris yang dipilih
           if (jTable1.getRowCount()==0) { // Memeriksa apakah tabel kosong
               //dikosongkan karena button di set false saat aplikasi pertama jalan
           }else{
               JOptionPane.showMessageDialog(null, "Pilih data yang dihapus");
           }
       }else{
           int a=JOptionPane.showConfirmDialog(null,"Yakin ingin menghapus data", "select", JOptionPane.YES_NO_OPTION);
            if (a==0)
            {
                model.removeRow(jTable1.getSelectedRow());
                JOptionPane.showMessageDialog(null, "Data berhasil di hapus ");
            }
 
       }

    }//GEN-LAST:event_buttonTableHapusActionPerformed

    private void buttonInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonInputActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); // Mengambil model tabel default
        if (textNama.getText().trim().equals("") || textHubungan.getText().trim().equals("") || jComboBox1.getSelectedItem().equals("Silahkan Input") || textAlamat.getText().trim().equals("") || textEmail.getText().trim().equals("") || textNohp.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data terlebih dahulu"); // Memeriksa apakah ada field input yang kosong
        }else{
            model.addRow(new Object[] {textNama.getText(), textHubungan.getText(), jComboBox1.getSelectedItem(), textAlamat.getText(), textEmail.getText(), textNohp.getText()}); // Menambahkan baris baru ke tabel dengan data dari input fields
            
            textNama.setText("");
            jComboBox1.setSelectedItem("Silahkan Input");
            textHubungan.setText("");
            textEmail.setText("");
            textAlamat.setText("");
            textNohp.setText("");
            
            buttonTableSimpan.setEnabled(true);
            buttonTableEdit.setEnabled(true);
            buttonTableHapus.setEnabled(true);
            
            JOptionPane.showMessageDialog(null, "Data berhasil di input");
        }
        
    }//GEN-LAST:event_buttonInputActionPerformed

    private void buttonTableEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTableEditActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); // Mengambil model tabel default
        if (jTable1.getSelectedRow()==-1) { // Memeriksa apakah ada baris yang dipilih
            if (jTable1.getRowCount()==0) { // Memeriksa apakah tabel kosong
                //dikosongkan karena button di set false saat aplikasi pertama jalan
            }else{
                JOptionPane.showMessageDialog(null, "Pilih data terlebih dulu");
            }
        }else{
            textNama.setText(model.getValueAt(jTable1.getSelectedRow(), 0).toString());
            textHubungan.setText(model.getValueAt(jTable1.getSelectedRow(), 1).toString());
            jComboBox1.setSelectedItem(model.getValueAt(jTable1.getSelectedRow(), 2).toString());
            textAlamat.setText(model.getValueAt(jTable1.getSelectedRow(), 3).toString());
            textEmail.setText(model.getValueAt(jTable1.getSelectedRow(), 4).toString());
            textNohp.setText(model.getValueAt(jTable1.getSelectedRow(), 5).toString());
            
            textNama.requestFocus();
            
            buttonInput.setEnabled(false);
        }
        
        
    }//GEN-LAST:event_buttonTableEditActionPerformed

