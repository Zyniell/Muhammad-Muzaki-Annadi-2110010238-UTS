    private void buttonTableSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTableSimpanActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); // Mengambil model tabel default
        if (jTable1.getSelectedRow()==-1) { // Memeriksa apakah ada baris yang dipilih
            if (jTable1.getRowCount()==0) { // Memeriksa apakah tabel kosong
                //dikosongkan karena button di set false saat aplikasi pertama jalan
            }else{
                JOptionPane.showMessageDialog(null, "Pilih data terlebih dulu");
            }
        }else{
            model.setValueAt(textNama.getText(), jTable1.getSelectedRow(), 0);
            model.setValueAt(textHubungan.getText(), jTable1.getSelectedRow(), 1);
            model.setValueAt(jComboBox1.getSelectedItem(), jTable1.getSelectedRow(), 2);
            model.setValueAt(textAlamat.getText(), jTable1.getSelectedRow(), 3);
            model.setValueAt(textEmail.getText(), jTable1.getSelectedRow(), 4);
            model.setValueAt(textNohp.getText(), jTable1.getSelectedRow(), 5);
            
            textNama.setText("");
            jComboBox1.setSelectedItem("Silahkan Input");
            textHubungan.setText("");
            textEmail.setText("");
            textAlamat.setText("");
            textNohp.setText("");
            
            buttonInput.setEnabled(true);
            
            JOptionPane.showMessageDialog(null, "Data tabel berhasil di edit");
            
        }
        
    }//GEN-LAST:event_buttonTableSimpanActionPerformed

